export function useTranslation(lang: Language = "en") {
  const currentTranslations = translations[lang] || translations.en
  
  // Create the t function with fallback
  const t = (key: keyof typeof translations.en): string => {
    return (currentTranslations as any)[key] || (translations.en as any)[key] || key
  }
  
  // Return both the function and the translations object for convenience
  // We use a Proxy to handle missing keys gracefully when using as an object
  return new Proxy({ t, language: lang } as any, {
    get(target, prop) {
      if (prop in target) return target[prop]
      return (currentTranslations as any)[prop] || (translations.en as any)[prop] || prop
    }
  })
}

export async function loadExternalTranslations() {
  if (typeof window === "undefined" || !window.electronAPI) return

  try {
    const appPath = await window.electronAPI.getAppPath()
    const localesPath = await window.electronAPI.exists("locales") 
      ? "locales" 
      : (await window.electronAPI.exists(pathJoin(appPath, "locales")) ? pathJoin(appPath, "locales") : null)

    if (!localesPath) {
      // Create locales directory if it doesn't exist
      try {
        const defaultLocalesPath = pathJoin(appPath, "locales")
        await window.electronAPI.createFolder(defaultLocalesPath)
      } catch (e) {
        console.warn("Failed to create locales folder", e)
      }
      return
    }

    const files = await window.electronAPI.readDirectory(localesPath)
    for (const file of files) {
      if (file.name.endsWith(".json")) {
        try {
          const content = await window.electronAPI.readFile(file.path)
          const json = JSON.parse(content)
          const langCode = file.name.replace(".json", "")
          
          if (json && typeof json === "object") {
            translations[langCode] = {
              ...(translations[langCode] || {}),
              ...json
            }
            console.log(`Loaded external translation: ${langCode}`)
          }
        } catch (e) {
          console.error(`Failed to load translation file: ${file.name}`, e)
        }
      }
    }
  } catch (e) {
    console.error("Failed to load external translations", e)
  }
}

function pathJoin(...parts: string[]) {
  return parts.join("/").replace(/\/+/g, "/")
}
